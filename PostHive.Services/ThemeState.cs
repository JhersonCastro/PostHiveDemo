namespace PostHive.Services
{
    /// <summary>
    /// Theme state class
    /// This class is used to manage the theme state of the application.
    /// It contains a property to determine if the dark theme is enabled.
    /// </summary>
    public class ThemeState
    {
        /// <summary>
        /// Gets or sets a value indicating whether the dark theme is enabled.
        /// </summary>
        public bool IsDark { get; set; }
    }
}
